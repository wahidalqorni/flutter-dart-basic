class Persegi{
  //double => tipe dari function
  //kPersegi() => nama function
  //(double panjang, double lebar) => parameter yg harus diinputkan saat funtion dipanggil/digunakan
  //kalo kita pake function yg bertipe, maka kita harus mereturn nilai dari fucntion tesebut
  // => arrow function 
  // { = biasa
  // return = biasa
  // } = biasa
  double kPersegi(double panjang, double lebar) =>  2 * (panjang + lebar); 
}